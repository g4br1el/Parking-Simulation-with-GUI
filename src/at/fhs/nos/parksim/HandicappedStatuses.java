package at.fhs.nos.parksim;

public enum HandicappedStatuses {
	/**
	 * Normal car, may use only a 
	 * single (normal) parking place 
	 */
	NORMAL,
	
	/**
	 * Car with status "handicap"! The
	 * driver may use special parking 
	 * places or is allowed to use
	 * up to two "normal" parking places
	 */
	HANDICAP
}
